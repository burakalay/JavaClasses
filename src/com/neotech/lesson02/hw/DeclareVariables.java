package com.neotech.lesson02.hw;

public class DeclareVariables {
	
	public static void main(String[] args) {
		
		int a = 15;
		boolean b = true;
		char c = 'F';
		byte d = 127;
		short e = 12543;
		long f = 1234567854564654690L;
		float pi1 = 3.14f;
		double pi2 = 3.14159265358979323846;
		
		
		
		System.out.println("a value : "+a);
		System.out.println("b value :" +b);
		System.out.println("starting character : " +c);
		System.out.println("d value :" +d);
		System.out.println("e value :" +e);
		System.out.println("f value :" +f);
		System.out.println("pi value for elemantery :" +pi1);
		System.out.println("pi value for college :" +pi2);
		
		
		
		
		
	}

}
