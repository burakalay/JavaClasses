package com.neotech.lesson09;

public class ForLoopIntro {

	public static void main(String[] args) {
		
		int num=1;
		
		while(num <=10) {
			
			System.out.println(num);
			num++;
		}
		
		System.out.println("-------------------------------------");
		
		// 3 importantpar
		//initilization, condition, increment
		
		for (int num2 =1; num2<=10; num2++) {
			
			System.out.println(num2);
		}

	}

}
